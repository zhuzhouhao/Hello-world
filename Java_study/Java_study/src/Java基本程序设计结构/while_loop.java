package Java基本程序设计结构;
import java.util.Scanner;

public class while_loop
{
    public static void main(String[] args)
    {
        Scanner in = new Scanner(System.in);
        int i = in.nextInt();
        for(int j = 0;j<i;j++)
        {
            System.out.println("hello world");
        }
    }
}